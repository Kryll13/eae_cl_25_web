pi = 3.14159
rayon = 2.2

# équation de l'aire d'un cercle <- ceci est un commentaire
aire = pi * (rayon**2)
print(aire)

# changer les valeurs du rayon <- un autre commentaire
# utilisez les commentaires pour aider les autres à comprendre ce que vous faites dans le code

rayon = rayon + 1
print(aire)     # l'aire ne change pas
aire = pi * (rayon**2)
print(aire)
